// Rust言語の教科書 — 第1章 フォーマット指定子
fn main() {
    let x = 42;
    let pi = 3.14159;

    // 基本の表示
    println!("値: {x}");

    // デバッグ表示（{:?}）
    let nums = [1, 2, 3];
    println!("配列: {nums:?}");

    // 小数点以下の桁数指定
    println!("円周率: {pi:.2}");

    // ゼロ埋め
    println!("ゼロ埋め: {x:05}");

    // 2進数・16進数
    println!("2進数: {x:b}, 16進数: {x:x}");
}
